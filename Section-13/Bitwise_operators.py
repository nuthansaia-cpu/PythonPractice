# Bitwise Operators

# AND Operator
a= 10 & 13
print(a)
Binary=format(a,'b')
print(Binary)

print("---------------------------------------------------")

# OR Operator
b= 10 | 13
print(b)
Binary=format(b,'b')
print(Binary)

print("---------------------------------------------------")

# XOR Operator
c= 10 ^ 13
print(c)
Binary=format(c,'b')
print(Binary)

print("---------------------------------------------------")

# Complement Operator
d= ~10
print(d)
Binary=format(d,'b')
print(Binary)

print("---------------------------------------------------")

# Left Shift Operator
e= 10 << 1
print(e)
e= 10 << 2
print(e)
e= 10 << 3
print(e)
e= 10 << 4
print(e)
e= 10 << 5
print(e)


print("---------------------------------------------------")

# Right Shift Operator
f= 10 >> 1
print(f)
f= 5 >> 1
print(f)
f= 10 >> 2
print(f)
f= 320 >> 5
print(f)

