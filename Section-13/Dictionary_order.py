#string order comparison
s1="software"
s2="Hardware"
print(s1>s2)

s1="python"
s2="pycharm"
print(s1<s2)

s1="integr" 
s2="integr"
print(s1==s2)

s1="printer"
s2="print"
print(s1>s2)
print("---------------------------------------------------------------------")
#Boolean order comparison
print(True>False)
print(True==True)
print(False<True)
print(False==False)
print("---------------------------------------------------------------------")
#integer order comparison
print(10>5)
print(10==10)   
print(5<10)
print(5!=10)
print(5>=5)
print(3<=2)
print("---------------------------------------------------------------------")
#float order comparison
print(10.5>5.2)
print(10.5==10.5)   
print(5.3<10.1)
print(5.3!=10.4)
print(5.6>=5.6)
print(3.2<=2.9)
print("---------------------------------------------------------------------")
#complex number comparison
print((2+3j)==(2+3j))
print((2+3j)!=(3+2j))
#Note: In Python, complex numbers cannot be compared using <, >, <=, >=.
